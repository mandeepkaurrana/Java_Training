//Steps:
//ğŸ“Œ To get a input from a user, we're using Scanner class.
//ğŸ“Œ int n = scanner.nextInt(), get the number of elements we want to store.
//ğŸ“Œ int[] array = new int[n];, creates an array with size n.
//ğŸ“Œ By iterating from 0 to i<n, we can get the elements from user in array[i].
//ğŸ“Œ Then, pass this array as a parameter to the findSmallestArray() method.
//ğŸ“Œ Where, we say Integer.MAX_VALUE as the smallest number.
//ğŸ“Œ Next, we are comparing array[i] less than smallest number. If so, we need to change the smallest number as array[i].
