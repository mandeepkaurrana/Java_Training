public class EqualityComparison {

	public static void main(String[] args) {
		
		//== 		!=

		int a=20;
		
		if(a==10)
			System.out.println("Equals to 10");
		else
			System.out.println("Not Equals to 10");
		
		if(a!=10)
			System.out.println("Not Equals to 10");
		else
			System.out.println("Equals to 10");
		
	}

}
