package remix;


//a++ - postfix inc
//++a - prefix inc
public class IncDecTip {

	public static void main(String[] args) {
		
		int a=10;
		
		int b=a++;
		System.out.println("a : "+a);
		System.out.println("b : "+b);
		
		
		int p=10;
		int c=++p;
		System.out.println("p : "+p);
		System.out.println("c : "+c);
		
		

	}

}
